package Hybrid_Inheritance;

public class Person {
	private int age;
	private String jobtype;
	public Person(int age, String jobtype) {
		super();
		this.age = age;
		this.jobtype = jobtype;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getJobtype() {
		return jobtype;
	}
	public void setJobtype(String jobtype) {
		this.jobtype = jobtype;
	}
	
	
	
	
}
