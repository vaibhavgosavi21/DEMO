package Single_level;

public class Fruit {
	private String types;
	private String name;
	public Fruit(String types, String name) {
		super();
		this.types = types;
		this.name = name;
	}
	public String getTypes() {
		return types;
	}
	public void setTypes(String types) {
		this.types = types;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	

}
